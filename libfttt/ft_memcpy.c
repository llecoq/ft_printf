#include "libft.h"

void	*ft_memcpy(char *dst, char *src, size_t n)
{
	size_t				i;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n)
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
